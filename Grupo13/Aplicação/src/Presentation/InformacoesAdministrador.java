        if(res[res.length-1].equals("Morador")){
            System.out.println(res[1]);
            ActorDAO.remActor(res[1]);
            Nome.setListData(ActorDAO.getActores());
            Aviso.setText("Morador removido.");
        }
        else
            Aviso.setText("Só pode remover Moradores.");
